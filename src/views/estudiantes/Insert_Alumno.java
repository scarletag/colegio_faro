    public Alumnos sendFormulario(){
        //Instanciar el objeto
        Alumnos alumno = null;
        try {
        String usuario, nombre, apellido, direccion, telefono ,correo;
        usuario = t_usuario.getText();
        nombre = t_nombre.getText();
        apellido = t_apellido.getText();
        direccion = t_direccion.getText();
        telefono = t_telefono.getText();
        correo = t_correo.getText();
        int rol = 1;
        int edad = Integer.parseInt(t_edad.getText());
        alumno = new Alumnos(usuario, randomPass, nombre, apellido, direccion, telefono, correo, edad, rol);
        } catch (Exception e) {
            e.printStackTrace(System.out);
        }
        return alumno;
        //System.out.println("Contraseña aleatoria: "+ randomPass + " contraseña encriptada: "+ contraEncriptada);
    }
    void limpiar(){
        t_usuario.setText("");
        t_nombre.setText("");
        t_apellido.setText("");
        t_direccion.setText("");
        t_telefono.setText("");
        t_correo.setText("");
        t_edad.setText("");
    }
    boolean validaciones(){
        boolean isValid = true;
        if(!!t_usuario.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Usuario vacio, complete los datos");
            return !isValid;
        }
        if(!!t_nombre.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Nombre vacio, complete los datos");
            return !isValid;
        }
        if(!!t_apellido.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Apellido vacio, complete los datos");
            return !isValid;
        }
        if(!!t_direccion.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "La direccion esta vacia, complete los datos");
            return !isValid;
        }
        if(!!t_telefono.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Telefono vacio, complete los datos");
            return !isValid;
        }
        if(!!t_edad.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Edad vacia, complete los datos");
            return !isValid;
        }
        if(!!t_correo.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Correo vacio, complete los datos");
            return !isValid;
        }
        return isValid;
    }
