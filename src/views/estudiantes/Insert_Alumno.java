    private void btn_registrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_registrarActionPerformed
        AlumnosSQL sql = new AlumnosSQL();
       if(!validaciones()){
           return;
       }
       int respuesta = sql.createAlumno(sendFormulario());
       if(respuesta == 0){
       JOptionPane.showMessageDialog(null, "No se pudo registrar el alumno");
            limpiar();
            return;
        }
        JOptionPane.showMessageDialog(null, "Alumno Registrado");
        JOptionPane.showMessageDialog(null, "La contraseña temporal es: "+randomPass);
        limpiar();

    }//GEN-LAST:event_btn_registrarActionPerformed

